// create a random variable between 1 and 100
// create a random variable between 1 and 50

let x=Math.floor(Math.random()*100+1);
let y =Math.floor(Math.random()*50+1);

let sum=x+y;
console.log(`Sum of ${x} and ${y} is ${sum}`);


let difference=x-y;
console.log(`Sum of ${x} and ${y} is ${difference}`);

let mul=x*y;
console.log(`Multiplication of ${x} and ${y} is ${mul}`);

let division=x/y;
console.log(`Division of ${x} and ${y} is ${division}`);

let remainder=x%y;
console.log(`Remainder of ${x} and ${y} is ${remainder}`);



